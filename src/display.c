// desenha caractere
void draw_char(int x, int y, char c) {
    if (c < 32 || c > 127) return;

    for (int i = 0; i < 5; i++) {
        uint8_t line = font[c - 32][i];
        for (int j = 0; j < 8; j++) {
            if (line & (1 << j)) {
                int index = x + i + ((y + j) / 8) * WIDTH;
                buffer[index] |= (1 << ((y + j) % 8));
            }
        }
    }
}

void display_draw_string(int x, int y, const char *str) {
    while (*str) {
        draw_char(x, y, *str++);
        x += 6;
    }
}

