#define I2C_PORT i2c1
#define SDA_PIN 14
#define SCL_PIN 15

#define SSD1306_ADDR _u(0x3C)

#define WIDTH 128
#define HEIGHT 64

static uint8_t buffer[WIDTH * HEIGHT / 8];  

// =======================
// Funções básicas I2C
// =======================

void ssd1306_command(uint8_t cmd) {
    uint8_t buf[2] = {0x00, cmd};
    i2c_write_blocking(I2C_PORT, SSD1306_ADDR, buf, 2, false);
}

void ssd1306_data(uint8_t *data, size_t len) {
    uint8_t temp[len + 1];
    temp[0] = 0x40;
    memcpy(&temp[1], data, len);
    i2c_write_blocking(I2C_PORT, SSD1306_ADDR, temp, len + 1, false);
}

// =======================
// Inicialização
// =======================

void display_init(void) {
    i2c_init(I2C_PORT, 400 * 1000);

    gpio_set_function(SDA_PIN, GPIO_FUNC_I2C);
    gpio_set_function(SCL_PIN, GPIO_FUNC_I2C);
    gpio_pull_up(SDA_PIN);
    gpio_pull_up(SCL_PIN);

    sleep_ms(100);

    ssd1306_command(0xAE); // OFF
    ssd1306_command(0x20);
    ssd1306_command(0x00);
    ssd1306_command(0xB0);
    ssd1306_command(0xC8);
    ssd1306_command(0x00);
    ssd1306_command(0x10);
    ssd1306_command(0x40);
    ssd1306_command(0x81);
    ssd1306_command(0xFF);
    ssd1306_command(0xA1);
    ssd1306_command(0xA6);
    ssd1306_command(0xA8);
    ssd1306_command(0x3F);
    ssd1306_command(0xD3);
    ssd1306_command(0x00);
    ssd1306_command(0xD5);
    ssd1306_command(0xF0);
    ssd1306_command(0xD9);
    ssd1306_command(0x22);
    ssd1306_command(0xDA);
    ssd1306_command(0x12);
    ssd1306_command(0xDB);
    ssd1306_command(0x20);
    ssd1306_command(0x8D);
    ssd1306_command(0x14);
    ssd1306_command(0xAF); // ON

    display_clear();
    display_update();
}

// =======================
// Buffer
// =======================

void display_clear(void) {
    memset(buffer, 0, sizeof(buffer));
}

void display_update(void) {
    for (int page = 0; page < 8; page++) {
        ssd1306_command(0xB0 + page);
        ssd1306_command(0x00);
        ssd1306_command(0x10);

        ssd1306_data(&buffer[WIDTH * page], WIDTH);
    }
}

