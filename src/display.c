// =======================

void display_main_screen(float temp, uint16_t joy, int pwm, bool wifi_ok, int mode) {
    char line[32];

    display_clear();

    sprintf(line, "Temp: %.1f C", temp);
    display_draw_string(0, 0, line);

    sprintf(line, "Joy : %d", joy);
    display_draw_string(0, 10, line);

    sprintf(line, "PWM : %d%%", pwm);
    display_draw_string(0, 20, line);

    sprintf(line, "WiFi: %s", wifi_ok ? "OK" : "OFF");
    display_draw_string(0, 30, line);

    sprintf(line, "Mode: %s", mode ? "AUTO" : "MAN");
    display_draw_string(0, 40, line);

    display_update();
}